// Escribe un programa que solicite al usuario ingresar un numero,
// vamos a mostrarle al usurio si ese numero es par o impar.

// El modulo % nos devuelve el resto

const numero = Number(prompt('Digita un numero')); // 4

if(numero % 2 === 0){
    document.write('Tu numero es par');
} else {
    document.write('Tu numero es impar');
}


